# برنامج يقوم بجمع العناصر الموجوده في الاقائمة 
numbers=[1,2,3,4,5]
print("Let's add each number to the next")
num=0

for n in numbers:
    num+=n
    print(f"-> {num}\n")
    print(f"The total number is: {num}")



